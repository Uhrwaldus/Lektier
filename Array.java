public class Array {
    public static void main(String[] args) {


        int[] vorTabel = { 5, 3, -2, 9, 0, -5, 10, 11, -7, 10 };



        int tæller=0;

        while ( tæller<vorTabel.length ) {
            System.out.print( vorTabel[tæller] + " " );
            tæller++;
        }

        System.out.println();


    }
}
